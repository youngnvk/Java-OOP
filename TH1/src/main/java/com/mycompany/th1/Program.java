    @SuppressWarnings("empty-statement")
    private void JbLuutruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbLuutruActionPerformed
        // TODO add your handling code here:

        try {
            File file = new File("sinhvien.dat");
            PrintWriter pf = new PrintWriter(file);
            for (Student x : students) {
                pf.printf("%-25s %-25s %-25s %-25s %-25s %-25s %-25.2f\n",
                        x.getHoTen(),
                        x.getNgaySinh(),
                        x.getDiaChi(),
                        x.getGioiTinh(),
                        x.getMaSV(),
                        x.getEmail(),
                        x.getDiemTK());

            }
            pf.close();
            JOptionPane.showMessageDialog(rootPane, "Ghi file thanh cong!");
        } catch (FileNotFoundException ex) {

        }
    }//GEN-LAST:event_JbLuutruActionPerformed

