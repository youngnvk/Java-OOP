    private void jbThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbThemActionPerformed
        // TODO add your handling code here:
        Student sv = new Student();
        if (!jtextHoten.getText().isEmpty()) {
            sv.setHoTen(jtextHoten.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Du lieu nhap khong hop le!");
            return;
        }
        if (!jtextNgaysinh.getText().isEmpty()) {
            sv.setNgaySinh(jtextNgaysinh.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Du lieu nhap khong hop le!");
            return;
        }
        if (!jtextDiachi.getText().isEmpty()) {
            sv.setDiaChi(jtextDiachi.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Du lieu nhap khong hop le!");
            return;
        }

        if (jChecknam.isSelected() && jChecknu.isSelected()) {
            JOptionPane.showMessageDialog(null, "Chi chon mot gioi tinh!");
            return;
        }

        if (jChecknam.isSelected()) {
            sv.setGioiTinh("Nam");
        } else if (jChecknu.isSelected()) {
            sv.setGioiTinh("Nu");
        } else {
            JOptionPane.showMessageDialog(null, "Du lieu nhap khong hop le");
            return;
        }
        if (!jtextMasv.getText().isEmpty()) {
            sv.setMaSV(jtextMasv.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Du lieu nhap khong hop le!");
            return;
        }
        if (!jtextEmail.getText().isEmpty()) {
            sv.setEmail(jtextEmail.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Du lieu nhap khong hop le!");
            return;
        }
        try {
            double b = Double.parseDouble(jtextDiemtk.getText());
            if (b <= 10 && b >= 0) {
                sv.setDiemTK(b);
            } else {
                JOptionPane.showMessageDialog(null, "Du lieu nhap khong hop le");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Du lieu nhap khong hop le!");
            return;
        }
        sv.hienThiThongTin();
        students.add(sv);
        JOptionPane.showMessageDialog(rootPane, "Them sinh vien thanh cong!");
    }//GEN-LAST:event_jbThemActionPerformed

