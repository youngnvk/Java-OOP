    private String check_Ten_Sach(int maSach) {
        for (Sach x : sachs) {
            if (x.getMaSach() == maSach) {
                return x.getTenSach();
            }
        }
        return null;
    }

    private String check_BanDoc(int maBanDoc) {
        for (BanDoc bd : bandocs) {
            if (bd.getMaBD() == maBanDoc) {
                return bd.getHoTen();  // Trả về tên của bạn đọc nếu tìm thấy
            }
        }
        return null;  // Trả về null nếu không tìm thấy
    }
    public boolean checkSLSach(int maSach, int requestedQuantity) {
        for (Sach x : sachs) {
            if (x.getMaSach() == maSach) {
                return requestedQuantity <= x.getSoLuong(); // Kiểm tra số lượng sách
            }
        }
        return false;
    }
    public int SLSach(int maSach) {
        for (Sach x : sachs) {
            if (x.getMaSach() == maSach) {
                return x.getSoLuong();
            }
        }
        return -1;
    }
