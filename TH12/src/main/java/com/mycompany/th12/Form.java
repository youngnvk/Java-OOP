    private void ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemActionPerformed
        QLMuonSach a = new QLMuonSach();
        String SachId = box2.getSelectedItem().toString();
        String id = box3.getSelectedItem().toString();

        // Cập nhật thông tin sách và bạn đọc
        a.getSach().setMaSach(Integer.parseInt(SachId));
        a.getSach().setTenSach(check_Ten_Sach(Integer.parseInt(SachId)));
        a.getBandoc().setMaBD(Integer.parseInt(id));
        a.getBandoc().setHoTen(check_BanDoc(Integer.parseInt(id)));

        int soluong = Integer.parseInt(sl.getText());

        // Kiểm tra số lượng sách trước khi tiếp tục
        boolean isAvailable = checkSLSach(Integer.parseInt(SachId), soluong);
        if (!isAvailable) {
            JOptionPane.showMessageDialog(null, "Số lượng sách không đủ, hiện có: " + SLSach(Integer.parseInt(SachId)));
            return;
        }

        a.setSoluong(soluong);

        boolean isUpdated = false;

        // Kiểm tra sách đã có trong danh sách chưa
        for (QLMuonSach x : qLmuonsachs) {
            if (a.getSach().getMaSach() == x.getSach().getMaSach()) {
                // Kiểm tra số lượng sách đã có cộng với số lượng sách muốn thêm
                int newsl = x.getSoluong() + a.getSoluong();
                int maxSoLuong = SLSach(Integer.parseInt(SachId)); // Số lượng tối đa cho phép

                if (newsl > maxSoLuong) {
                    JOptionPane.showMessageDialog(null, "Số lượng sách yêu cầu vượt quá số lượng có sẵn. Hiện có: " + maxSoLuong);
                    return; // Nếu số lượng vượt quá, không cho phép thêm
                }

                // Nếu sách đã tồn tại, cập nhật số lượng
                x.setSoluong(newsl);

                // Cập nhật lại bảng
                for (int i = 0; i < tableModel_2.getRowCount(); i++) {
                    if (tableModel_2.getValueAt(i, 0).equals(x.getSach().getMaSach())) {
                        tableModel_2.setValueAt(newsl, i, 4);  // Cập nhật số lượng trong bảng
                        break;
                    }
                }
                isUpdated = true;
                break; // Nếu đã cập nhật xong, thoát khỏi vòng lặp
            }
        }

        // Nếu sách chưa tồn tại trong danh sách, thêm sách mới vào
        if (!isUpdated) {
            qLmuonsachs.add(a);
            // Thêm dòng mới vào bảng
            Object[] row = new Object[]{
                a.getSach().getMaSach(),
                a.getSach().getTenSach(),
                a.getBandoc().getMaBD(),
                a.getBandoc().getHoTen(),
                a.getSoluong()
            };
            tableModel_2.addRow(row);
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
        }

        tableModel_2.fireTableDataChanged();
    }//GEN-LAST:event_ThemActionPerformed

