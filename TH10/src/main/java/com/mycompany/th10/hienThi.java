    public void timKiemTheoLop(ArrayList<LopHoc> lophocs, String tenlop) {
        int check = 0;
        for (LopHoc lop : lophocs) {
            if (lop.getLop().equalsIgnoreCase(tenlop)) {
                for (SinhVien sv : lop.getDsSV()) {
                    Object[] row = new Object[]{
                        sv.getHoTen(),
                        sv.getMaSV(),
                        sv.getDiemQT(),
                        sv.getDiemHP(),
                        lop.getLop(),
                        sv.getDiemTB()
                    };
                    tableModel.addRow(row);
                    tableModel.fireTableDataChanged();                    
                } 
            }
            check = 1;
        }
        if(check == 0){
            JOptionPane.showMessageDialog(rootPane, "Khong tim thay");
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Da tim thay");
        }
    }

