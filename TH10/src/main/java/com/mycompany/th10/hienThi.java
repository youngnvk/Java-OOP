    public void hienThiDuLieu(ArrayList<LopHoc> lophocs) {
        for (LopHoc lop : lophocs) {
            for (SinhVien sv : lop.getDsSV()) {
                Object[] row = new Object[]{
                    sv.getHoTen(),
                    sv.getMaSV(),
                    sv.getDiemQT(),
                    sv.getDiemHP(),
                    lop.getLop(),
                    sv.getDiemTB()
                };
                tableModel.addRow(row);
                tableModel.fireTableDataChanged();
            }
        }

    }
